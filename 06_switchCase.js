var day = 3;
switch (day) {
  case 1:
    console.log(`Monday`);
    break;
  case 2:
    console.log(`Tue`);
    break;
  case 3:
    console.log(`Wed`);
    break;
  case 4:
    console.log(`Thur`);
    break;
  case 5:
    console.log(`Fri`);
    break;
  case 6:
    console.log(`Sat`);
    break;
  case 7:
    console.log(`Sunday`);
    break;
  default:
    console.log(`Invalid inputs...`);
    break;
}
